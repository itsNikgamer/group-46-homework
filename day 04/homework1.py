def display_data_types():
    sample_string = "Hello"
    sample_integer = 123
    sample_float = 45.67
    sample_boolean = True
    
    print(f"Type of {sample_string}: {type(sample_string)}")
    print(f"Type of {sample_integer}: {type(sample_integer)}")
    print(f"Type of {sample_float}: {type(sample_float)}")
    print(f"Type of {sample_boolean}: {type(sample_boolean)}")

display_data_types()